//=============================================================================
void UninitResult(void)
{
	UninitResultTex();
}

//=============================================================================
// 更新処理
//=============================================================================
void UpdateResult(void)
{
	UpdateResultTex();
}



//=============================================================================
// 描画処理
//=============================================================================
void DrawResult(void)
{






	// 2Dの物を描画する処理
	{
		// Z比較なし
		SetDepthEnable(false);

		// ライティングを無効
		SetLightEnable(false);

		//ここから下に
		DrawResultTex();

		// ライティングを有効に
		SetLightEnable(true);

		// Z比較あり
		SetDepthEnable(true);
	}
}